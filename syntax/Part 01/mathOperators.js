// 1. Suma
suma = 5 + 3;
console.log("Suma:", suma);  // 8

// 2. Resta
resta = 10 - 4;
console.log("Resta:", resta);  // 6

// 3. Multiplicación
multiplicacion = 7 * 6;
console.log("Multiplicación:", multiplicacion);  // 42

// 4. División
division = 20 / 4;
console.log("División:", division);  // 5

// 5. Resto de división
resto = 10 % 3;
console.log("Resto de división:", resto);  // 1

// 6. Exponente
exponente = 2 ** 3;
console.log("Exponente:", exponente);  // 8