a = 5;
b = 10;

// 1. Comparación: Mayor que
console.log("a es mayor que b:", a > b);  // false

// 2. Comparación: Menor que
console.log("a es menor que b:", a < b);  // true

// 3. Comparación: Mayor o igual que
console.log("a es mayor o igual que b:", a >= b);  // false

// 4. Comparación: Menor o igual que
console.log("a es menor o igual que b:", a <= b);  // true

// 5. Comparación: Igual a
console.log("a es igual a b:", a === b);  // false

// 6. Comparación: No igual a
console.log("a no es igual a b:", a !== b);  // true

// 7. Operador lógico AND
console.log("a es menor que 10 y mayor que 0:", a < 10 && a > 0);  // true

// 8. Operador lógico OR
console.log("a es menor que 0 o mayor que 10:", a < 0 || a > 10);  // false